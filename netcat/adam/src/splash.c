void splash(void)
{
  unsigned short delay=DELAY_TIME;
  
  /* // Blank the screen */
  vdp_noblank();

  // Set default color for text
  vdp_color(VDP_INK_WHITE,VDP_INK_DARK_BLUE,VDP_INK_DARK_BLUE);

  // unpack splash graphics and put them on screen
  RLEUnpack(MODE2_ATTR,splash_colors,MODE2_MAX);
  RLEUnpack(0x0000,splash_pixels,MODE2_MAX);
  
  // Put in text
  gotoxy(DETAILS_X,DETAILS_Y);
  cprintf("    RELEASED UNDER GPL v. 3\n    SEE LICENSE FOR DETAILS");
						 
  gotoxy(NAME_X,NAME_Y);
  textbackground(VDP_INK_MAGENTA);
  cprintf("FUJILINK I");

  
  /* // Turn screen back on */
  vdp_blank();
  
  // Wait for a moment
  sleep(3);

  state=DIRECTORY;
}
