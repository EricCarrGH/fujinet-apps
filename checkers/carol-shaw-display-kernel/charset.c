/**
 * @brief pulling out charset for Checkers
 */

#include <stdio.h>

unsigned char charset[1024] =
  {
    0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00, // BLANK (0) 
    0x3C,0x7E,0xFF,0xFF,0xFF,0xFF,0x7E,0x3C, // CHECKER (1) 
    0x3C,0x7E,0xA5,0xA5,0xC3,0xC3,0x7E,0x3C, // KING (2) 
    0xC3,0x66,0x3C,0x18,0x18,0x3C,0x66,0xC3, // CURSOR (3) 
    0x00,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0x00, // BORDER (4)
  };

void main(void)
{
  FILE *fp = fopen("checkers.fnt","w");
  fwrite(charset,sizeof(unsigned char),sizeof(charset),fp);
  fclose(fp);
}
